async function main() {
  console.log("🚀 FAQ 데이터 시드 시작...");

  // 기존 FAQ 삭제
  await prisma.fAQ.deleteMany({});
  console.log("✅ 기존 FAQ 데이터 삭제 완료");

  // FAQ 데이터 생성
  for (let i = 0; i < faqData.length; i++) {
    await prisma.fAQ.create({
      data: {
        category: faqData[i].category,
        question: faqData[i].question,
        answer: faqData[i].answer,
        orderNum: i + 1,
        isActive: true,
      },
    });
  }

  console.log(`✅ ${faqData.length}개의 FAQ 생성 완료`);
  console.log("🎉 FAQ 시드 완료!");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
